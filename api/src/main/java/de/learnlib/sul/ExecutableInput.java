package de.learnlib.sul;

import de.learnlib.exception.SULException;

/**
 * An executable input is an input that performs a specific action on a {@link SUL} itself.
 *
 * @param <O>
 *         output
 */
public interface ExecutableInput<O> {

    /**
     * Executes {@code this} input symbol.
     *
     * @return the output generated by the {@link SUL}
     *
     * @throws SULException
     *         if {@code this} input cannot be executed on the {@link SUL}
     */
    O execute();

}
